// While
let index=0
while(index<=10){
    console.log(`The value of index is ${index}`);
    index=index+2
}
let myarray=["flash","batman","superman"]
let arr=0
while(arr<myarray.length){
    console.log(`The value of array is ${myarray[arr]}`);
    arr++
}

// DO While
let score=1
do {
    console.log(`The score is ${score}`);
    score++
} while (score<=10);